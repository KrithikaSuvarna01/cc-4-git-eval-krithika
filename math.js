//This file contains Javascript functions to add two numbers and to subract two numbers.
/**
 * This Function adds 2 Numbers which is passed as argument to the function
 * @param {*} number1 
 * @param {*} number2 
 * @returns a numbers that is the sum of 2 numbers
 */
function add(number1,number2){
    return number1+number2
}

/**
 * Function subtracts number2 from number1 and returns the result 
 * @param {*} number1 is the number which is being subtracted by
 * @param {*} number2 is the number which is used to subtract
 * @returns the difference between 2 numbers 
 */
function subtract(number1,number2){
    return number1-number2
}